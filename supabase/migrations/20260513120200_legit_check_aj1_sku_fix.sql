-- Air Jordan 1 High OG имеет ДВА исторически легитимных формата стиль-кода:
--   1. Современный: 'DZ5485-612' (2 буквы + 4 цифры + дефис + 3 цифры)
--   2. Классический: '555088-XXX' (6 цифр + дефис + 3 цифры)
--
-- Предыдущий regex принимал только первый формат, из-за чего AI-pipeline
-- стал бы помечать оригиналы с классическим SKU как "сомнительные".
--
-- Также правим текст auth_marker, чтобы он явно упоминал оба формата.

UPDATE legit_check_models
SET
  sku_pattern = '^([A-Z]{2}[0-9]{4}|[0-9]{6})-[0-9]{3}$',
  auth_markers = ARRAY[
    'Стиль-код в правом верхнем углу размерной бирки: тонкий шрифт, формат "XX0000-000" (новые) или "555088-XXX" (классика)',
    'Текст "AIR" на пятке: чёткий, центрирован вертикально, ровные засечки',
    'Swoosh: ровные стежки 7-9 на см, без следов клея',
    'Jumpman на язычке: симметричный, мяч-баскетбол ровно круглый',
    'Insole stamp: "AIR JORDAN" чёткий, без размытия',
    'Размерная бирка: BR/EUR/UK/US — тонкий шрифт, без наклона'
  ]
WHERE slug = 'air-jordan-1-high-og'
  AND brand_id = (SELECT id FROM legit_check_brands WHERE slug='nike');
